package ex2_switch;

public class Ex3_switch {
	public static void main(String[] args) {
		//만약 switch문에 break가 없다면 어떻게 될까??
		int num = 1;
		switch(num) {
		case 1:
			System.out.println(num+"은 1입니다.");
		case 3:
			System.out.println(num+"은 3입니다.");
		case 7:
			System.out.println(num+"은 7입니다.");
		}
		
		//조건에 맞는 case를 시작으로 뒤따라오는 모든 case구문이
		//실행됩니다. 따라서 개발자는 break; 키워드를 적절하게 
		//이용할 수 있어야 합니다.
		
		
		
		
		
		
		
		
		
	}
}
