package ex2_this;

public class Student {
	String name;
	int age;
	int studentID;
	//this
	//객체 스스로를 참조하는 키워드
	//객체가 자기 자신을 참조하는데 사용하는 변수
	//this.멤버를 사용하면 매개변수와 멤버를 구분하여 사용할 수 있다.
	
	public Student(String name, int age, int studentID) {
		this.name = name;
		this.age = age;
		this.studentID = studentID;
	}
	
	
	
	
	
	
}
